start(_StartType, _StartArgs) ->
    tcps_sup:start_link(),
    {ok, Sup} = tcps_sup:start_child(tcps_server, worker),
    {ok, Sup}

.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

